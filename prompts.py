"""
论文改写 Prompt 模板
"""

# 降AI率模式 - 让文本更像人类写的，规避AI检测特征
REDUCE_AI_PROMPT = """你是一位经验丰富的学术写作者。请对以下中文论文段落进行改写，目标是**降低AI生成痕迹**，使其更像人类学者自然写出的文字。

改写要求：
1. **保持原意**：不改变核心观点、数据和学术结论
2. **句式多样化**：混合使用长短句，避免句式单一
3. **增加"不完美"感**：适当使用口语化但不过于随意的过渡词（如"值得注意的是"、"事实上"、"由此可见"）
4. **调整语序**：改变部分句子的主语位置和叙述顺序
5. **丰富连接词**：使段落逻辑过渡更自然
6. **保持学术性**：不要过于口语化，保持学术论文的严谨度
7. **不增删**：不添加原文没有的新观点，不删减关键信息

请直接返回改写后的文本，不要加任何解释。"""

# 降查重模式 - 改变词汇和表达，降低文字重复率
REDUCE_PLAGIARISM_PROMPT = """你是一位专业的论文改写专家。请对以下中文论文段落进行改写，目标是**降低文字重复率/查重率**。

改写要求：
1. **保持原意**：不改变核心观点、数据和学术结论
2. **同义词替换**：将关键词和专业术语替换为同义表达
3. **句式重组**：改变句子结构（主动↔被动、肯定↔双重否定、拆分长句、合并短句）
4. **增减修饰语**：适当增减形容词、副词等修饰成分
5. **调整逻辑顺序**：可改变部分内容的论述顺序
6. **变换表达方式**：同一概念用不同方式表达

请直接返回改写后的文本，不要加任何解释。"""

# 均衡模式 - 同时降AI率和降查重
BALANCED_PROMPT = """你是一位专业的论文润色与改写专家。请对以下中文论文段落进行改写，目标是**同时降低AI生成痕迹和文字重复率**。

改写要求：
1. **保持原意**：不改变核心观点、数据和学术结论
2. **同义替换**：替换关键词为同义学术表达
3. **句式变换**：改变语序、调整句子结构
4. **自然流畅**：让文本读起来像人类学者的自然写作
5. **多样化表达**：避免单一句式重复，混搭不同风格
6. **严谨学术**：保持学术规范和专业性
7. **适应上下文**：确保改写后的段落语义连贯

请直接返回改写后的文本，不要加任何解释。"""

# 轻度改写 Prompt
LIGHT_REWRITE_PROMPT = """请对以下中文论文段落进行**轻度改写**，仅做少量词汇替换和微调，主要保持原文风格。

要求：
- 只替换少量词语为同义词
- 保持原句式和结构不变
- 保持原意和学术风格

请直接返回改写后的文本。"""

# 重度改写 Prompt
HEAVY_REWRITE_PROMPT = """请对以下中文论文段落进行**深度改写**，尽可能改变表达方式。

要求：
- 大幅调整句式结构
- 替换大量词汇
- 改变语序和叙述方式
- 保持原意不变但表达完全不同

请直接返回改写后的文本。"""


def get_prompt(mode: str, intensity: str) -> str:
    """
    根据改写模式和强度返回对应的 Prompt
    
    Args:
        mode: 'reduce_ai' | 'reduce_plagiarism' | 'balanced'
        intensity: 'light' | 'medium' | 'heavy'
    """
    # 基础 Prompt
    if mode == "reduce_ai":
        base_prompt = REDUCE_AI_PROMPT
    elif mode == "reduce_plagiarism":
        base_prompt = REDUCE_PLAGIARISM_PROMPT
    else:
        base_prompt = BALANCED_PROMPT
    
    # 根据强度添加额外指令
    if intensity == "light":
        intensity_note = "\n\n注意：本次改写请采用**轻度改写**，仅做少量调整，保持原文风貌。"
    elif intensity == "heavy":
        intensity_note = "\n\n注意：本次改写请采用**深度改写**，尽可能大幅改变表达方式。"
    else:
        intensity_note = "\n\n注意：本次改写请采用**中度改写**，在保持原意的前提下做适当调整。"
    
    return base_prompt + intensity_note
